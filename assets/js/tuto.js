const galleryCard = document.querySelector('.gallery');

const swipeCarrousel =  (event) => {
    
    if (event.target.className == "las la-angle-right") {
        const carrouselElement = event.target.previousSibling;
        const len = carrouselElement.childNodes.length;
        const step = carrouselElement.scrollWidth/len;
        carrouselElement.scrollLeft += step;
    } else {
        const carrouselElement = event.target.nextSibling;
        const len = carrouselElement.childNodes.length;
        const step = carrouselElement.scrollWidth/len;
        carrouselElement.scrollLeft -= step;
    }
}

